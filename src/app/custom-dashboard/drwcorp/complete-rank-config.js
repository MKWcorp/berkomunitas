// Tier system configuration
export const tierRanges = {
  TOP3: { min: 1, max: 3, fontSize: 20 },
  LORD: { min: 4, max: 8, fontSize: 12 },
  GUARD: { min: 9, max: 111, fontSize: 9 }
};

// Function to get rank configuration
export const getRankConfig = (rank) => {
  const position = exactFigmaPositions[rank];
  if (!position) return null;
  
  return {
    rank,
    x: position.x,
    y: position.y,
    width: position.width,
    height: position.height,
    fontSize: tierRanges[position.tier].fontSize,
    backgroundColor: position.bg,
    tier: position.tier,
    textColor: position.bg === '#181A19' ? '#FFFFFF' : '#000000' // White text for dark background
  };
};

// Function to get tier style for React component
export const getTierStyle = (rank) => {
  const config = getRankConfig(rank);
  if (!config) return {};
  
  return {
    position: 'absolute',
    left: `${config.x}px`,
    top: `${config.y}px`,
    width: `${config.width}px`,
    height: `${config.height}px`,
    backgroundColor: config.backgroundColor,
    color: config.textColor,
    borderRadius: '5px',
    display: 'flex',
    alignItems: 'center',
    justifyContent: 'center',
    fontFamily: 'Poppins',
    fontWeight: '500',
    fontSize: `${config.fontSize}px`,
    opacity: config.tier === 'GUARD' ? 0.8 : (config.tier === 'LORD' ? 0.9 : 1),
    border: config.tier === 'TOP3' ? '2px solid rgba(255, 192, 58, 0.3)' : 'none',
    boxShadow: config.tier === 'TOP3' ? '0 4px 8px rgba(0,0,0,0.1)' : 'none'
  };
};

export default {
  exactFigmaPositions,
  tierRanges,
  getRankConfig,
  getTierStyle
};
